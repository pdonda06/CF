// Utility functions for dynamic question selection
export const getRandomQuestions = (questions, count = 10) => {
  const shuffled = [...questions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getQuestionsByDifficulty = (questions, difficulty) => {
  return questions.filter(q => q.difficulty === difficulty);
};

export const getQuestionsByCategory = (questions, category) => {
  return questions.filter(q => q.category === category);
};

export const getMixedDifficultyQuestions = (questions, count = 10) => {
  const easyQuestions = getQuestionsByDifficulty(questions, 'easy');
  const mediumQuestions = getQuestionsByDifficulty(questions, 'medium');
  const hardQuestions = getQuestionsByDifficulty(questions, 'hard');
  
  const easyCount = Math.ceil(count * 0.4);
  const mediumCount = Math.ceil(count * 0.4);
  const hardCount = count - easyCount - mediumCount;
  
  const selectedQuestions = [
    ...getRandomQuestions(easyQuestions, easyCount),
    ...getRandomQuestions(mediumQuestions, mediumCount),
    ...getRandomQuestions(hardQuestions, hardCount)
  ];
  
  return getRandomQuestions(selectedQuestions, count);
};
